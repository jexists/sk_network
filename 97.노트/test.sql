-- 테이블 생성 및 데이터 삽입
DROP TABLE IF EXISTS SVC_JOIN;
CREATE TABLE SVC_JOIN (
    CUST_ID VARCHAR(10) NOT NULL,
    SVC_ID VARCHAR(5) NOT NULL,
    JOIN_YMD VARCHAR(8) NOT NULL,
    JOIN_HH VARCHAR(4) NOT NULL,
    SVC_START_DATE DATE NULL,
    SVC_END_DATE DATE NULL
);

INSERT INTO SVC_JOIN VALUES ('C001', 'SVC01', '20141201', '00', '2014-12-01', '2015-01-15');
INSERT INTO SVC_JOIN VALUES ('C002', 'SVC01', '20141201', '00', '2014-12-01', '2015-01-20');
INSERT INTO SVC_JOIN VALUES ('C003', 'SVC02', '20141201', '00', '2014-12-01', '2015-01-25');
INSERT INTO SVC_JOIN VALUES ('C004', 'SVC02', '20141201', '01', '2014-12-01', '2015-02-01');

-- ① CONCAT 사용
SELECT SVC_ID, COUNT(*) AS CNT
FROM SVC_JOIN
WHERE SVC_END_DATE >= '2015-01-01'
  AND SVC_END_DATE <= '2015-01-31'
  AND CONCAT(JOIN_YMD, JOIN_HH) = '2014120100'
GROUP BY SVC_ID;

-- ② IN 사용
SELECT SVC_ID, COUNT(*) AS CNT
FROM SVC_JOIN
WHERE SVC_END_DATE >= '2015-01-01'
  AND SVC_END_DATE < '2015-02-01'
  AND JOIN_YMD = '20141201'
  AND JOIN_HH = '00'
GROUP BY SVC_ID;

-- ③ DATE_FORMAT 사용 (Oracle TO_CHAR → MySQL DATE_FORMAT으로 변환)
SELECT SVC_ID, COUNT(*) AS CNT
FROM SVC_JOIN
WHERE '201501' = DATE_FORMAT(SVC_END_DATE, '%Y%m')
  AND JOIN_YMD = '20141201'
  AND JOIN_HH = '00'
GROUP BY SVC_ID;

-- ④  대신 CONCAT 사용 (Oracle
 → MySQL CONCAT으로 변환)
SELECT SVC_ID, COUNT(*) AS CNT
FROM SVC_JOIN
WHERE DATE_FORMAT(SVC_END_DATE, '%Y%m') = '201501'
  AND CONCAT(JOIN_YMD, JOIN_HH) = '2014120100'
GROUP BY SVC_ID;
